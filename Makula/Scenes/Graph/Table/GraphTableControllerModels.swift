/// The diagnosis table cell representations.
enum GraphTableData: String {
	/// The navigation view as a cell.
	case navigation = "GraphCellNavigation"
	/// The static text cell showing the eye's type.
	case eye = "GraphCellEye"
	/// The static text cell showing the date of the selected point.
	case date = "GraphCellDate"
	/// The diagram cell showing the graph.
	case graph = "GraphCellGraph"
}
